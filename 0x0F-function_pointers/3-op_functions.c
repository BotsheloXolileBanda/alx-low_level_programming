#include "3-calc.h"

/**
  * op_add - Addition handler.
  *
  * @a: Operand.
  * @b: Operand.
  * Return: Result.
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  * op_sub - Subtraction handler.
  *
  * @a: Operand.
  * @b: Operand.
  * Return: Result.
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  * op_mul - Multiplication handler.
  *
  * @a: Operand.
  * @b: Operand.
  * Return: Result.
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  * op_div - Division handler.
  *
  * @a: Operand.
  * @b: Operand.
  * Return: Result.
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  * op_mod - Modulos handler.
  *
  * @a: operand.
  * @b: Operand.
  * Return: Result.
  */
int op_mod(int a, int b)
{
	return (a % b);
}
